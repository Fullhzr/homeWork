package com.xmbestone.qss.util;

public class FolderConfiguration {
	
	private String terminalDeviceImageFolder;
	
	private String personImageFolder;
	
	private String goodsImageFolder;
	
	private String workGuideFileFolder;
	
	private String tempFileFolder;
	
	

	

	public String getTempFileFolder() {
		return tempFileFolder;
	}

	public void setTempFileFolder(String tempFileFolder) {
		this.tempFileFolder = tempFileFolder;
	}

	public String getTerminalDeviceImageFolder() {
		return terminalDeviceImageFolder;
	}

	public void setTerminalDeviceImageFolder(String terminalDeviceImageFolder) {
		this.terminalDeviceImageFolder = terminalDeviceImageFolder;
	}

	public String getPersonImageFolder() {
		return personImageFolder;
	}

	public void setPersonImageFolder(String personImageFolder) {
		this.personImageFolder = personImageFolder;
	}

	public String getGoodsImageFolder() {
		return goodsImageFolder;
	}

	public void setGoodsImageFolder(String goodsImageFolder) {
		this.goodsImageFolder = goodsImageFolder;
	}

	public String getWorkGuideFileFolder() {
		return workGuideFileFolder;
	}

	public void setWorkGuideFileFolder(String workGuideFileFolder) {
		this.workGuideFileFolder = workGuideFileFolder;
	}

}
